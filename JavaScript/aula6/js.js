function fundoAzul(){
    let element = document.querySelector('.texto')

    //usando apropiedade style
    element.style.backgroundColor = "blue"
}
function fundoTrasnparent(){
    document.querySelector('.texto').style.backgroundColor = "transparent"
}
function fonteVermelha(){
    document.querySelector('.texto').style.color = "red"
}
function fonteNormal(){
    document.querySelector('.texto').style.color = "black"
}